#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;

	i = 0;
	if (ft_strlen((char *)s1) != ft_strlen((char *)s2))
		return (ft_strlen((char *)s1) - ft_strlen((char *)s2));
	while (s1[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

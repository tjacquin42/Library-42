#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	j;

	if (s)
	{
		i = 0;
		j = ft_strlen(s);
		while (i < j)
		{
			s[i] = '\0';
			i++;
		}
	}
}
